public class Sample06a {
    public static void main(String[] args) {

        // 配列の準備
        int[] nums = new int[5];

        // 配列にデータを格納
        // キーワード：要素、添字、要素数
        nums[0] = 10;
        nums[1] = 20;
        nums[2] = 40;
        nums[3] = 60;
        nums[4] = 80;

        // 要素へアクセスし出力
        System.out.println(nums[2]);
    }
}